#lesson 01 : data type
#type() : show you the type of data

print(10)
# to show data type we use type()
# integer
print(type(10))
print(type(-100))

# float
print(type(100.5))

#string
print(type("Hello"))

#list
print(type([1,2,3,4,5])) # list

#tuple
print(type((1,2,3,7,8))) #tuple

#dictionary
print(type({"one" : 1, "two" : 2, "three" : 3})) # dict => dictionary

#boolean
print(type(2==2))
print(type(2==3))



# Integer Numbers
print(type(10))
print(type(-20))
print(type(10.20))

myComplexNumber = 5+6j
print(type(myComplexNumber))
print("Real Part is : {}".format(myComplexNumber.real))
print("Imaginary Part is : {}".format(myComplexNumber.imag))

